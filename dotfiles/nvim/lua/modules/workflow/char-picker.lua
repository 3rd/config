local insert_character = function(char, started_in_insert)
  local mode = vim.api.nvim_get_mode().mode:sub(1, 1)

  if started_in_insert and mode ~= "i" then
    local keys = vim.api.nvim_replace_termcodes("a" .. char, true, false, true)
    vim.api.nvim_feedkeys(keys, "n", true)
    return
  end

  vim.api.nvim_put({ char }, "c", true, true)
end

local pick_and_insert = function(items, prompt)
  local started_in_insert = vim.api.nvim_get_mode().mode:sub(1, 1) == "i"

  vim.ui.select(items, {
    prompt = prompt,
    format_item = function(entry)
      return entry.label
    end,
  }, function(selection)
    if not selection then return end
    vim.schedule(function()
      insert_character(selection.char, started_in_insert)
    end)
  end)
end

local insert_unicode = function()
  pick_and_insert(unicode_items, "Insert unicode")
end

local insert_emoji = function()
  pick_and_insert(emoji_items, "Insert emoji")
end

local insert_char_prefix = function()
  local ok, key = pcall(vim.fn.getcharstr)
  if not ok or not key or key == "" then return end

  local ctrl_v = vim.api.nvim_replace_termcodes("<C-v>", true, false, true)

  if key == "v" or key == "V" or key == ctrl_v then
    insert_unicode()
    return
  end

  if key == "e" or key == "E" then
    insert_emoji()
    return
  end

  local fallback_keys = vim.api.nvim_replace_termcodes("<C-v>" .. key, true, false, true)
  vim.api.nvim_feedkeys(fallback_keys, "n", true)
end

local insert_char = function(opts)
  local mode = vim.trim((opts and opts.args) or "")
  if mode == "" or mode == "unicode" then
    insert_unicode()
    return
  end
  if mode == "emoji" then
    insert_emoji()
    return
  end
  vim.notify("InsertChar supports: unicode | emoji", vim.log.levels.WARN)
end

return lib.module.create({
  name = "workflow/char-picker",
  hosts = "*",
  setup = function()
    vim.api.nvim_create_user_command("InsertUnicode", insert_unicode, { desc = "Insert Unicode character" })
    vim.api.nvim_create_user_command("InsertEmoji", insert_emoji, { desc = "Insert emoji" })
    vim.api.nvim_create_user_command("InsertChar", insert_char, {
      nargs = "?",
      desc = "Insert character from unicode or emoji picker",
      complete = function()
        return { "unicode", "emoji" }
      end,
    })
  end,
  mappings = {
    { "i", "<C-v>", insert_char_prefix, "Character picker prefix: v=unicode e=emoji" },
    { "n", "<leader>iu", insert_unicode, "Insert Unicode character" },
    { "n", "<leader>ie", insert_emoji, "Insert emoji" },
  },
  exports = {
    insert_unicode = insert_unicode,
    insert_emoji = insert_emoji,
  },
})
